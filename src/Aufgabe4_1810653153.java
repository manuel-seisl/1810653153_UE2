public class Aufgabe4_1810653153
{

    public static void main(String[] args)
    {

        for(int i = 1; i <= 10; i++)
        {
            System.out.println("Der Schleifenzähler ist: " + i);
        }

        int j = 1;

        while(j <= 10)
        {
            System.out.println("Der Schleifenzähler ist: " + j);
            j++;
        }

        int k = 1;

        do {
            System.out.println("Der Schleifenzähler ist: " + k);
            k++;
        } while (k <= 10);

    }

}
